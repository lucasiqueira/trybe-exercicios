package app;

public class SomaNumeros {

  /**
   * Retorna a soma entre dois números.
   *
   * @param numA um número inteiro.
   * @param numB outro número inteiro
   * @return a soma entre os dois números.
   */
  public static int retornaSomaEntreDoisNumeros(int numA, int numB) {
    int soma = numA + numB;
    return soma;
  }
}
